package command

import (
	"io"
	"os"
	"path"

	"github.com/spf13/cobra"
)

func newNew() *cobra.Command {
	shebang := "#!/bin/bash"
	dir, err := os.Getwd()
	if err != nil {
		panic(err)
	}
	packageName := path.Base(dir)

	cmd := &cobra.Command{
		Use:  "new",
		Args: cobra.ExactArgs(1),
		RunE: func(_ *cobra.Command, args []string) error {
			filename := args[0]

			io.WriteString(os.Stdout, shebang)
			io.WriteString(os.Stdout, "\n")

			io.WriteString(os.Stdout, `cd "$(dirname $0)"`+"\n")

			io.WriteString(os.Stdout, "\n")

			io.WriteString(os.Stdout, `render_file() {
	echo "// Code generated by './`+filename+`.sh'. DO NOT EDIT."
	echo "package `+packageName+`"
	# TODO: generate code
}

render_file | goimports \
	> `+filename+`.go`)
			io.WriteString(os.Stdout, "\n")

			return nil
		},
	}

	flags := cmd.Flags()
	flags.StringVar(&packageName, "package", packageName, "Name of package")

	return cmd
}
